chunk.reader <- function(source, max.line = 65536L, sep=NULL) .Call(create_chunk_reader, if(inherits(source, "connection") || inherits(source, "fileDescriptor")) source else file(as.character(source)[1], "rb"), max.line, sep)

read.chunk <- function(reader, max.size = 33554432L, timeout=Inf) .Call(chunk_read, reader, max.size, timeout)
